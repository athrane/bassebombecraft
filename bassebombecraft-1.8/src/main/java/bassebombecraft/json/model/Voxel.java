package bassebombecraft.json.model;

public class Voxel {

	public final int x;
	public final int y;
	public final int z;
	public final String blockName;
	
	public Voxel(int x, int y, int z, String blockName) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
		this.blockName = blockName;
	}
	
	
	
}
